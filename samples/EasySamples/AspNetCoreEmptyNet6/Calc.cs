namespace AspNetCoreEmptyNet6
{
    public class Calc
    {
        public int Sum(int a, int b)
        {
            return a + b;
        }

        public int Dec(int a, int b)
        {
            return a - b;
        }

        public int Multi(int a, int b)
        {
            return a * b;
        }
    }
}
